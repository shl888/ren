from typing import Dict

# ----- 标准字段顺序 -----
"""
标准字段顺序定义
用途：让输出的数据字段顺序整齐，便于查看路由数据

为什么需要这个？
    - MongoDB 存储的字段顺序是随机的（内部优化）
    - 修复区读取数据后，字段顺序不固定
    - 路由接口看到的数据字段顺序混乱，看着别扭
    - 这个函数统一排序，让输出更整齐

注意：
    - 这个顺序不影响任何业务逻辑
    - 只影响输出的显示顺序
    - 只用在修复区推送数据前
"""

